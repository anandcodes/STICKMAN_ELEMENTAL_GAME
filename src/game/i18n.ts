export type UiTextKey = keyof typeof TRANSLATIONS.en;

export function t(locale: Locale, key: UiTextKey, vars?: Vars): string {
  const dictionary = TRANSLATIONS[locale] ?? TRANSLATIONS.en;
  const template = dictionary[key] ?? TRANSLATIONS.en[key];
  if (!vars) return template;

  return template.replace(/\{(\w+)\}/g, (_match, varName: string) => {
    const value = vars[varName];
    return value === undefined ? '' : String(value);
  });
}

export function elementName(locale: Locale, element: 'fire' | 'water' | 'earth' | 'wind'): string {
  if (element === 'fire') return t(locale, 'element_fire');
  if (element === 'water') return t(locale, 'element_water');
  if (element === 'earth') return t(locale, 'element_earth');
  return t(locale, 'element_wind');
}
